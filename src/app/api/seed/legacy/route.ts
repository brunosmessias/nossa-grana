function unixToTimestamp(unix: number): Date {
  return new Date(unix * 1000);
}

export async function GET() {
  try {
    const log: string[] = [];

    const validTransactions = oldTransactions.filter((tx) => {
      if (tx.transaction_date < 0) {
        log.push(
          `Skipped corrupted date: ${tx.description} (${tx.transaction_date})`,
        );
        return false;
      }
      if (tx.transaction_date > 1780000000) {
        log.push(
          `Skipped far-future date: ${tx.description} (${tx.transaction_date})`,
        );
        return false;
      }
      return true;
    });

    const validFamilies = oldFamilies.filter((f) => {
      const hasData =
        validTransactions.some((tx) => tx.family_id === f.id) ||
        oldCategories.some((c) => c.family_id === f.id);
      if (!hasData) {
        log.push(`Skipped empty family: ${f.name} (${f.id})`);
      }
      return hasData;
    });

    log.push(
      `Data: ${validFamilies.length} families, ${oldFamilyMembers.length} members, ${oldCategories.length} categories, ${validTransactions.length} transactions`,
    );

    // Step 1+4 combined: Find real owners first, then insert families with valid ownerUserId
    const realOwnerMap = new Map<string, string>();
    for (const m of oldFamilyMembers) {
      const existingUser = await db.query.user.findFirst({
        where: eq(user.email, m.email),
      });
      if (existingUser) {
        realOwnerMap.set(m.family_id, existingUser.id);
      }
    }

    // Deduplicate: one family per user. If multiple old families map to the same user,
    // keep the first one and remap the others' transactions/categories to it.
    const userToFamily = new Map<string, string>();
    const familyRemap = new Map<string, string>();
    for (const f of validFamilies) {
      const realOwnerId = realOwnerMap.get(f.id);
      if (!realOwnerId) {
        log.push(`Skipped family "${f.name}" (${f.id}) — no matching user found`);
        continue;
      }
      const existingFamilyId = userToFamily.get(realOwnerId);
      if (existingFamilyId) {
        familyRemap.set(f.id, existingFamilyId);
        log.push(`Consolidated family "${f.name}" (${f.id}) into ${existingFamilyId} — same owner ${realOwnerId}`);
      } else {
        userToFamily.set(realOwnerId, f.id);
      }
    }

    // Apply remapping to realOwnerMap, transactions, categories
    const finalValidFamilyIds = new Set(userToFamily.values());
    for (const tx of validTransactions) {
      if (familyRemap.has(tx.family_id)) {
        (tx as { family_id: string }).family_id = familyRemap.get(tx.family_id)!;
      }
    }
    for (const c of oldCategories) {
      if (familyRemap.has(c.family_id)) {
        (c as { family_id: string }).family_id = familyRemap.get(c.family_id)!;
      }
    }

    for (const [familyId] of familyRemap) {
      realOwnerMap.delete(familyId);
    }

    const dedupedFamilies = validFamilies.filter((f) => finalValidFamilyIds.has(f.id));
    for (const f of dedupedFamilies) {
      const realOwnerId = realOwnerMap.get(f.id)!;
      await db
        .insert(families)
        .values({
          id: f.id,
          name: f.name,
          ownerUserId: realOwnerId,
          createdAt: unixToTimestamp(f.created_at),
          updatedAt: unixToTimestamp(f.created_at),
        })
        .onConflictDoNothing();
    }
    log.push(`Step 1: ${dedupedFamilies.length} families inserted (consolidated from ${validFamilies.length})`);

    // Step 2: Default CHECKING accounts
    const accountIdMap = new Map<string, string>();
    for (const f of dedupedFamilies) {
      const accountId = crypto.randomUUID();
      accountIdMap.set(f.id, accountId);
      await db.insert(accounts).values({
        id: accountId,
        familyId: f.id,
        name: "Conta Corrente",
        type: "CHECKING",
        icon: "wallet",
        color: "#1866e4",
        initialBalanceCents: 0,
        archived: false,
      });
    }
    log.push(`Step 2: ${dedupedFamilies.length} default accounts created`);

    // Step 3: Categories
    for (const c of oldCategories) {
      const familyExists = dedupedFamilies.some((f) => f.id === c.family_id);
      if (!familyExists) {
        log.push(
          `Skipped category "${c.name}" (family ${c.family_id} not imported)`,
        );
        continue;
      }
      await db
        .insert(categories)
        .values({
          id: c.id,
          familyId: c.family_id,
          name: c.name,
          kind: c.type as "INCOME" | "EXPENSE",
          icon: c.icon,
          color: c.color,
          createdAt: unixToTimestamp(c.created_at),
        })
        .onConflictDoNothing();
    }
    log.push(`Step 3: ${oldCategories.length} categories processed`);

    // Step 4: Family members (link by email to existing auth users)
    let membersImported = 0;
    for (const m of oldFamilyMembers) {
      const targetFamilyId = familyRemap.get(m.family_id) ?? m.family_id;
      if (!finalValidFamilyIds.has(targetFamilyId)) continue;
      const existingUser = await db.query.user.findFirst({
        where: eq(user.email, m.email),
      });

      if (existingUser) {
        await db
          .insert(familyMembers)
          .values({
            familyId: targetFamilyId,
            userId: existingUser.id,
            role: "OWNER",
            createdAt: unixToTimestamp(m.joined_at),
          })
          .onConflictDoNothing();
        membersImported++;
      } else {
        log.push(
          `User not found: ${m.email} (${m.name}) — needs to sign up first`,
        );
      }
    }
    log.push(`Step 4: ${membersImported} members linked`);

    // Step 5: Transactions
    let txImported = 0;
    let txSkipped = 0;
    for (const tx of validTransactions) {
      const accountId = accountIdMap.get(tx.family_id);
      if (!accountId) {
        txSkipped++;
        continue;
      }

      if (!tx.category_id) {
        txSkipped++;
        continue;
      }

      await db
        .insert(transactions)
        .values({
          id: tx.id,
          familyId: tx.family_id,
          accountId,
          categoryId: tx.category_id,
          type: tx.type as "INCOME" | "EXPENSE",
          description: tx.description,
          amountCents: tx.amount_cents,
          transactionAt: unixToTimestamp(tx.transaction_date),
        })
        .onConflictDoNothing();

      txImported++;
    }
    log.push(
      `Step 5: ${txImported} transactions imported, ${txSkipped} skipped`,
    );

    return NextResponse.json({ ok: true, log });
  } catch (error) {
    const message = error instanceof Error ? error.message : "Unexpected error";
    return NextResponse.json({ ok: false, error: message }, { status: 500 });
  }
}
